		AND O.RESOURCE_ID IN (
			SELECT RESOURCE_ID
			FROM LIVESupplemental..ResourceID_ReportSettings S
			WHERE S.REPORT_NAME = 'Surface Area Paint'
				AND S.REPORT_SECTION = 'Main Report'
		)
	GROUP BY LT.WORKORDER_TYPE
		, LT.WORKORDER_BASE_ID
		, LT.WORKORDER_LOT_ID
		, LT.WORKORDER_SPLIT_ID
