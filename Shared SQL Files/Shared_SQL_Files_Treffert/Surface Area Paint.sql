	AND O.WORKORDER_SUB_ID = LT.WORKORDER_SUB_ID
WHERE W.TYPE = 'W'
	AND W.[STATUS] <> 'C'
	AND W.STATUS_EFF_DATE >= @STARTDATE 
	AND W.STATUS_EFF_DATE < @ENDDATE 

