use datawarehouse
go

declare @startdate datetime = getdate() - 7,
	@enddate datetime = getdate() - 1

-- VISUAL ENTERPRISE > PARTS REPORTS > Surface Area Paint
WITH CTE_BASE AS 
(SELECT W.BASE_ID
	, W.LOT_ID
	, W.SPLIT_ID
	, W.SUB_ID
	, CASE
		WHEN W.PART_ID LIKE '%REWORK%' THEN W2.PART_ID
		WHEN W.PART_ID LIKE '%TOOLING%' THEN W2.PART_ID
		WHEN W.PART_ID IS NULL THEN W2.PART_ID
		ELSE W.PART_ID
	END AS PARTID
	, W.PART_ID AS CHILDPART
FROM WORK_ORDER W
INNER JOIN WORK_ORDER W2
	ON W.TYPE = W2.TYPE
	AND W.BASE_ID = W2.BASE_ID
	AND W.LOT_ID = W2.LOT_ID
	AND W.SPLIT_ID = W2.SPLIT_ID
	AND W2.SUB_ID = '0'
WHERE W.TYPE = 'W'
)

SELECT 'MAIN PART' AS REPT
	, CAST(W.STATUS_EFF_DATE AS DATE) TRANSACTION_DATE
	, DATEPART(YYYY, W.STATUS_EFF_DATE) TRANSACTION_DATE_YEAR
	, DATEPART(WK, W.STATUS_EFF_DATE) TRANSACTION_DATE_WEEK
	, DATEPART(D, W.STATUS_EFF_DATE) TRANSACTION_DATE_DAY
	, W.BASE_ID
	, W.LOT_ID
	, W.SPLIT_ID
	, W.SUB_ID
	, PARTID
	, CHILDPART
	, W.STATUS
	, W.ENGINEERED_BY
	, O.RESOURCE_ID
	, SR.USER_5
	, CASE 
		WHEN PSA.MATERIAL_TYPE IS NULL THEN 'NEED DIMENSIONS' 
		ELSE PSA.MATERIAL_TYPE
	END AS MATERIAL_TYPE
	, PSA.ALLOY
	, W.[DESIRED_QTY]
	, PSA.THICKNESS_VAL
	, PSA.LENGTH_VAL
	, PSA.WIDTH_VAL
	, W.DESIRED_QTY * ISNULL(PSA.SURFACE_AREA, 0) AS SURFACE_AREA
FROM WORK_ORDER AS W
INNER JOIN CTE_BASE CTE
	ON W.TYPE = 'W'
	AND W.BASE_ID = CTE.BASE_ID
	AND W.LOT_ID = CTE.LOT_ID
	AND W.SPLIT_ID = CTE.SPLIT_ID
	AND W.SUB_ID = CTE.SUB_ID
INNER JOIN OPERATION O
	ON W.TYPE = O.WORKORDER_TYPE
	AND W.BASE_ID = O.WORKORDER_BASE_ID
	AND W.LOT_ID = O.WORKORDER_LOT_ID
	AND W.SPLIT_ID = O.WORKORDER_SPLIT_ID
	AND W.SUB_ID = O.WORKORDER_SUB_ID
INNER JOIN SHOP_RESOURCE SR
	ON SR.ID = O.RESOURCE_ID
	AND O.RESOURCE_ID IN (
		SELECT RESOURCE_ID
		FROM LIVESupplemental..ResourceID_ReportSettings S
		WHERE S.REPORT_NAME = 'Surface Area Paint'
			AND S.REPORT_SECTION = 'Main Report'
	)
LEFT OUTER JOIN SKILLS_PART_SURFACE_AREA AS PSA
	ON CTE.PARTID = PSA.PART_ID
WHERE W.TYPE = 'W'
	AND W.[STATUS] = 'C'
	AND W.STATUS_EFF_DATE >= @STARTDATE 
	AND W.STATUS_EFF_DATE < @ENDDATE 

UNION

-- CANCELLED/WIP WITH LABOR TICKETS
SELECT 'LABOR TICKETS MAIN' AS REPT
