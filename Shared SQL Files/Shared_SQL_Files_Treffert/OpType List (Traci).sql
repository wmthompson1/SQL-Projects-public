SELECT [ORDER_TYPE]
, CASE 
	WHEN (OPERATION_BINARY LIKE '%10-79%' OR OPTYPE_BINARY LIKE '%10-79%') THEN '10-79'
	WHEN (OPERATION_BINARY LIKE '%10-60%' OR OPTYPE_BINARY LIKE '%10-60%') THEN '10-60'
	WHEN (OPERATION_BINARY LIKE '%10-11%' OR OPTYPE_BINARY LIKE '%10-11%') THEN '10-11'
	ELSE ''
END AS PAINT
, CASE
	WHEN (OPERATION_BINARY LIKE '%TYPE 1&2%' OR OPTYPE_BINARY LIKE '%TYPE 1&2%') THEN 'TYPE 1&2'
	WHEN (OPERATION_BINARY LIKE '%TYPE 2 & 3%' OR OPTYPE_BINARY LIKE '%TYPE 2 & 3%') THEN 'TYPE 2 & 3'
	WHEN (OPERATION_BINARY LIKE '%TYPE 2 AND 3%' OR OPTYPE_BINARY LIKE '%TYPE 2 AND 3%') THEN 'TYPE 2 AND 3'
	WHEN (OPERATION_BINARY LIKE '%TYPE II%' OR OPTYPE_BINARY LIKE '%TYPE II%') THEN 'TYPE II'
	WHEN (OPERATION_BINARY LIKE '%TYPE I%' OR OPTYPE_BINARY LIKE '%TYPE I%') THEN 'TYPE I'
	WHEN (OPERATION_BINARY LIKE '%TYPE 1%' OR OPTYPE_BINARY LIKE '%TYPE 1%') THEN 'TYPE 1'
	WHEN (OPERATION_BINARY LIKE '%TYPE 2%' OR OPTYPE_BINARY LIKE '%TYPE 2%') THEN 'TYPE 2'
	WHEN (OPERATION_BINARY LIKE '%TYPE 3%' OR OPTYPE_BINARY LIKE '%TYPE 3%') THEN 'TYPE 3'
	ELSE ''
END AS PAINT_TYPE
, CTE.CUSTOMER_ID
, CTE.CUST_ORDER_ID
, CTE.COL_LINE
, CTE.COL_ORDER_QTY
, CTE.TOTAL_SHIPPED_QTY
, T.BASE_ID
, T.LOT_ID
, T.SPLIT_ID
, T.SUB_ID
, T.DESIRED_WANT_DATE
, T.PART_ID
, P.BUYER_USER_ID
--, SEQUENCE_NO
, T.RESOURCE_ID
, T.USER_2
, T.USER_3
, T.USER_4
, T.USER_5
--, T.OPERATION_TYPE
, T.OPERATION_BINARY
, T.OPTYPE_BINARY
FROM #T3 T
LEFT OUTER JOIN #CTE_COMBINED CTE
ON T.BASE_ID = CTE.BASE_ID
AND T.LOT_ID = CTE.LOT_ID
AND T.SPLIT_ID = CTE.SPLIT_ID
AND T.SUB_ID = CTE.SUB_ID
LEFT OUTER JOIN PART P
ON T.PART_ID = P.ID
GROUP BY [ORDER_TYPE]
, CTE.CUSTOMER_ID
, CTE.CUST_ORDER_ID
, CTE.COL_LINE
, CTE.COL_ORDER_QTY
, CTE.TOTAL_SHIPPED_QTY
, T.BASE_ID
, T.LOT_ID
, T.SPLIT_ID
, T.SUB_ID
, T.DESIRED_WANT_DATE
, T.PART_ID
, P.BUYER_USER_ID
--, SEQUENCE_NO
, T.RESOURCE_ID
, T.USER_2
, T.USER_3
, T.USER_4
, T.USER_5
--, T.OPERATION_TYPE
, T.OPERATION_BINARY
, T.OPTYPE_BINARY
ORDER BY CTE.CUSTOMER_ID
, CTE.CUST_ORDER_ID
, CTE.COL_LINE


