DECLARE @STARTDATE	DATE = '10/30/2018'	
IF OBJECT_ID('tempdb..#t1') IS NOT NULL DROP TABLE #t1	
IF OBJECT_ID('tempdb..#t2') IS NOT NULL DROP TABLE #t2
IF OBJECT_ID('tempdb..#t3') IS NOT NULL DROP TABLE #t3
IF OBJECT_ID('tempdb..#CTE_COMBINED') IS NOT NULL DROP TABLE #CTE_COMBINED
	
select WO.BASE_ID	
	, WO.LOT_ID
	, WO.SPLIT_ID
	, WO.SUB_ID
	, WO.DESIRED_WANT_DATE
	, WO.PART_ID
	, O.SEQUENCE_NO
	, O.RESOURCE_ID
	, SR.USER_2
	, SR.USER_3
	, SR.USER_4
	, SR.USER_5
	, O.OPERATION_TYPE
into #t1
from WORK_ORDER WO	
INNER JOIN OPERATION O	
	ON WO.BASE_ID = O.WORKORDER_BASE_ID
	AND WO.LOT_ID = O.WORKORDER_LOT_ID
	AND WO.SPLIT_ID = O.WORKORDER_SPLIT_ID
	AND WO.SUB_ID = O.WORKORDER_SUB_ID
	AND WO.[TYPE] = O.WORKORDER_TYPE
INNER JOIN SHOP_RESOURCE SR	
	ON SR.ID = O.RESOURCE_ID
	AND SR.USER_2 IN ('PAINT')	
	AND SR.USER_5 = 'AP1'	
WHERE WO.DESIRED_WANT_DATE >= @STARTDATE	
AND WO.[STATUS] IN ('C', 'R')	

SELECT T.* 
	, CAST(CAST(OB.BITS AS VARBINARY(MAX)) AS NVARCHAR(MAX)) OPERATION_BINARY
	, CAST(CAST(OTB.BITS AS VARBINARY(MAX)) AS NVARCHAR(MAX)) OPTYPE_BINARY
INTO #T2
FROM #T1 T
LEFT OUTER JOIN OPER_TYPE_BINARY OTB	
	ON T.OPERATION_TYPE = OTB.OPERATION_TYPE_ID
	AND OTB.SITE_ID = 'SK01'
	AND OTB.[TYPE] = 'D'
LEFT OUTER JOIN OPERATION_BINARY OB	
	ON T.BASE_ID = OB.WORKORDER_BASE_ID
	AND T.LOT_ID = OB.WORKORDER_LOT_ID
	AND T.SPLIT_ID = OB.WORKORDER_SPLIT_ID
	AND T.SUB_ID = OB.WORKORDER_SUB_ID
	AND T.SEQUENCE_NO = OB.SEQUENCE_NO

SELECT *
INTO #T3
FROM #T2
WHERE (OPERATION_BINARY LIKE '%10-79%' OR OPTYPE_BINARY LIKE '%10-79%')
OR (OPERATION_BINARY LIKE '%10-60%' OR OPTYPE_BINARY LIKE '%10-60%')
OR (OPERATION_BINARY LIKE '%10-11%' OR OPTYPE_BINARY LIKE '%10-11%')


