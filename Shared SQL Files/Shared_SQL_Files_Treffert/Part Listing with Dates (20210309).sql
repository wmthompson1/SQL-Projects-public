; WITH CTE_WOCLOSE AS
(SELECT WO.PART_ID, MAX(WO.CLOSE_DATE) CLOSE_DATE
	FROM WORK_ORDER WO
	LEFT OUTER JOIN REQUIREMENT R
	ON WO.BASE_ID = R.WORKORDER_BASE_ID
	AND WO.LOT_ID = R.WORKORDER_LOT_ID
	AND WO.SPLIT_ID = R.WORKORDER_SPLIT_ID
	AND WO.SUB_ID = R.WORKORDER_SUB_ID
	AND WO.[TYPE] = R.WORKORDER_TYPE
	--WHERE WO.PART_ID = P.ID
	--OR R.PART_ID = P.ID
GROUP BY WO.PART_ID
UNION
SELECT R.PART_ID, MAX(WO.CLOSE_DATE) CLOSE_DATE
	FROM REQUIREMENT R
	LEFT OUTER JOIN WORK_ORDER WO
	ON WO.BASE_ID = R.WORKORDER_BASE_ID
	AND WO.LOT_ID = R.WORKORDER_LOT_ID
	AND WO.SPLIT_ID = R.WORKORDER_SPLIT_ID
	AND WO.SUB_ID = R.WORKORDER_SUB_ID
	AND WO.[TYPE] = R.WORKORDER_TYPE
GROUP BY R.PART_ID
UNION
SELECT CP.PART_ID, MAX(WO.CLOSE_DATE) CLOSE_DATE
	FROM CO_PRODUCT CP
	LEFT OUTER JOIN WORK_ORDER WO
	ON WO.BASE_ID = CP.WORKORDER_BASE_ID
	AND WO.LOT_ID = CP.WORKORDER_LOT_ID
	AND WO.SPLIT_ID = CP.WORKORDER_SPLIT_ID
	AND WO.SUB_ID = CP.WORKORDER_SUB_ID
	AND WO.[TYPE] = CP.WORKORDER_TYPE
GROUP BY CP.PART_ID
)
