SELECT 
	L.WAREHOUSE_ID, 
	L.ID, 
	PL.PART_ID, 
	PL.QTY, 
	P.COMMODITY_CODE, 
	P.PURCHASED
FROM   
	PART P
INNER JOIN 
	PART_LOCATION PL
	ON P.ID = PL.PART_ID
INNER JOIN 
	LOCATION L
	ON PL.LOCATION_ID = L.ID
	AND PL.WAREHOUSE_ID = L.WAREHOUSE_ID
WHERE	PL.QTY <> 0
ORDER BY 
	L.WAREHOUSE_ID, 
	L.ID

