ON DSL.[DEMAND_TYPE] = 'OP'
AND DSL.DEMAND_BASE_ID = WO.BASE_ID
AND DSL.DEMAND_LOT_ID = WO.LOT_ID
AND DSL.DEMAND_SPLIT_ID = WO.SPLIT_ID
AND DSL.DEMAND_SUB_ID = WO.SUB_ID
--AND DSL.DEMAND_SEQ_NO = O.SEQUENCE_NO
LEFT OUTER JOIN PURC_ORDER_LINE POL
ON DSL.SUPPLY_BASE_ID = POL.PURC_ORDER_ID
AND DSL.SUPPLY_SEQ_NO = POL.LINE_NO
AND DSL.[SUPPLY_TYPE] = 'PO'
LEFT OUTER JOIN [LIVE].[dbo].PURCHASE_ORDER PO
ON PO.ID = DSL.SUPPLY_BASE_ID
WHERE WO.PART_ID != 'REWORK MFG'
AND WO.CREATE_DATE >= '1/1/2020'
AND WO.[STATUS] = 'C'

AND WO.BASE_ID IN ('1610070')--('1618677')
--('1594233', '1342914')--, '1463115', '1581739', '1583115', '1584517')
--AND S.SHIPPED_DATE >= @START
--AND SL.SHIPPED_QTY <> 0
--AND DSL.SUPPLY_BASE_ID IS NULL
--AND COL.ORDER_QTY > 0
AND EXISTS (SELECT 1
	FROM [LIVE].[dbo].OPERATION O
	WHERE WO.BASE_ID = O.WORKORDER_BASE_ID
	AND WO.LOT_ID = O.WORKORDER_LOT_ID
	AND WO.SPLIT_ID = O.WORKORDER_SPLIT_ID
	AND WO.SUB_ID = O.WORKORDER_SUB_ID
	AND WO.[TYPE] = O.WORKORDER_TYPE
	AND O.RESOURCE_ID = 'CONTRACTOR'
	)
GROUP BY WO.[TYPE]
, WO.BASE_ID
, WO.LOT_ID
, WO.SPLIT_ID
, WO.SUB_ID
