; WITH OP_BINARY AS
(SELECT OB.WORKORDER_BASE_ID
	, OB.WORKORDER_LOT_ID
	, OB.WORKORDER_SPLIT_ID
	, OB.WORKORDER_SUB_ID
	, OB.WORKORDER_TYPE
	, OB.SEQUENCE_NO
	, CONVERT(NVARCHAR(MAX), CONVERT(VARBINARY(MAX), OB.BITS)) AS SPECS
FROM OPERATION_BINARY OB
WHERE OB.WORKORDER_TYPE IN (@WO_TYPE)
	AND CONVERT(NVARCHAR(MAX), CONVERT(VARBINARY(MAX), OB.BITS)) LIKE '%' + @SPECS + '%'
	AND OB.BITS IS NOT NULL
	AND EXISTS(SELECT 1 FROM WORK_ORDER W 
		WHERE W.[STATUS] IN (@WO_STATUS)
			AND W.BASE_ID = OB.WORKORDER_BASE_ID
			AND W.LOT_ID = OB.WORKORDER_LOT_ID
			AND W.SUB_ID = OB.WORKORDER_SUB_ID
			AND W.SPLIT_ID = OB.WORKORDER_SPLIT_ID
			AND W.[TYPE] = OB.WORKORDER_TYPE
			AND W.PART_ID LIKE REPLACE(ISNULL(@PART_ID, '%') + '%', '%%', '%')
			AND W.CREATE_DATE >= ISNULL(@STARTDATE, '1/1/2020')
			AND W.CREATE_DATE <= ISNULL(@ENDDATE, GETDATE())
		)
)

, WO_BINARY AS
(SELECT WB.WORKORDER_BASE_ID
	, WB.WORKORDER_LOT_ID
	, WB.WORKORDER_SPLIT_ID
	, WB.WORKORDER_SUB_ID
	, WB.WORKORDER_TYPE
	, CONVERT(NVARCHAR(MAX), CONVERT(VARBINARY(MAX), WB.BITS)) AS SPECS 
FROM WORKORDER_BINARY WB
WHERE WB.WORKORDER_TYPE IN (@WO_TYPE)
	AND CONVERT(NVARCHAR(MAX), CONVERT(VARBINARY(MAX), WB.BITS)) LIKE '%' + @SPECS + '%'
	AND EXISTS(SELECT 1 FROM WORK_ORDER W 
		WHERE W.[STATUS] IN (@WO_STATUS)
			AND W.BASE_ID = WB.WORKORDER_BASE_ID
			AND W.LOT_ID = WB.WORKORDER_LOT_ID
			AND W.SUB_ID = WB.WORKORDER_SUB_ID
			AND W.SPLIT_ID = WB.WORKORDER_SPLIT_ID
			AND W.[TYPE] = WB.WORKORDER_TYPE
			AND W.PART_ID LIKE REPLACE(ISNULL(@PART_ID, '%') + '%', '%%', '%')
			AND W.CREATE_DATE >= ISNULL(@STARTDATE, '1/1/2020')
			AND W.CREATE_DATE <= ISNULL(@ENDDATE, GETDATE())
		)
)
