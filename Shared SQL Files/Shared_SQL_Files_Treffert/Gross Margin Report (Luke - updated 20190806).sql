		, ((T.QTY * -1) * ((WO.ACT_BURDEN_COST + WO.ACT_MATERIAL_COST + WO.ACT_LABOR_COST + WO.ACT_SERVICE_COST) / WO.DESIRED_QTY)) AS TOTAL_TRACE_COST
	FROM SHIPPER_LINE SL
	INNER JOIN TRACE_INV_TRANS  T
		ON SL.TRANSACTION_ID = T.TRANSACTION_ID
	INNER JOIN CUST_ORDER_LINE COL
		ON SL.CUST_ORDER_ID = COL.CUST_ORDER_ID
		AND SL.CUST_ORDER_LINE_NO = COL.LINE_NO
		AND COL.PART_ID IS NOT NULL
	INNER JOIN RECEIVABLE_LINE RL
		ON RL.CUST_ORDER_ID = COL.CUST_ORDER_ID
		AND RL.CUST_ORDER_LINE_NO = COL.LINE_NO
		AND RL.PACKLIST_ID = SL.PACKLIST_ID 
		AND RL.PACKLIST_LINE_NO = SL.LINE_NO
		AND RL.PACKLIST_ID IS NOT NULL
	INNER JOIN SHIPPER S
		ON S.PACKLIST_ID = SL.PACKLIST_ID 
	INNER JOIN RECEIVABLE R
		ON RL.INVOICE_ID = R.INVOICE_ID
	INNER JOIN CUSTOMER_ORDER CO
		ON COL.CUST_ORDER_ID = CO.ID
	LEFT OUTER JOIN DEMAND_SUPPLY_LINK DSL
		ON DSL.DEMAND_TYPE = 'CO'
		AND DSL.DEMAND_BASE_ID = COL.CUST_ORDER_ID
		AND DSL.DEMAND_SEQ_NO = COL.LINE_NO
		AND DSL.SUPPLY_BASE_ID IS NULL
	LEFT OUTER JOIN WORK_ORDER WO
		ON (T.TRACE_ID = WO.BASE_ID + '/' + WO.LOT_ID)
		AND WO.[TYPE] = 'W'
		AND WO.[STATUS] = 'C'
		AND WO.DESIRED_QTY <> 0
		AND WO.SPLIT_ID = '0'
		AND WO.SUB_ID = '0'
	WHERE S.SHIPPED_DATE >= @STARTDATE
		AND SL.SHIPPED_QTY <> 0
	----
	UNION ALL
	----
	SELECT COL.CUST_ORDER_ID
			, COL.LINE_NO
		, T.TRACE_ID
		, ((T.QTY * -1) * (WO.ACT_BURDEN_COST / WO.DESIRED_QTY)) AS TRACE_BURDEN_COST
		, ((T.QTY * -1) * (WO.ACT_MATERIAL_COST / WO.DESIRED_QTY)) AS TRACE_MATERIAL_COST
		, ((T.QTY * -1) * (WO.ACT_LABOR_COST / WO.DESIRED_QTY)) AS TRACE_LABOR_COST
		, ((T.QTY * -1) * (WO.ACT_SERVICE_COST / WO.DESIRED_QTY)) AS TRACE_SERVICE_COST
		, ((T.QTY * -1) * ((WO.ACT_BURDEN_COST + WO.ACT_MATERIAL_COST + WO.ACT_LABOR_COST + WO.ACT_SERVICE_COST) / WO.DESIRED_QTY)) AS TOTAL_TRACE_COST
	FROM SHIPPER_LINE SL
	INNER JOIN TRACE_INV_TRANS  T
		ON SL.TRANSACTION_ID = T.TRANSACTION_ID
	INNER JOIN CUST_ORDER_LINE COL
		ON SL.CUST_ORDER_ID = COL.CUST_ORDER_ID
		AND SL.CUST_ORDER_LINE_NO = COL.LINE_NO
		AND COL.PART_ID IS NOT NULL
	INNER JOIN RECEIVABLE_LINE RL
		ON RL.CUST_ORDER_ID = COL.CUST_ORDER_ID
		AND RL.CUST_ORDER_LINE_NO = COL.LINE_NO
		AND RL.PACKLIST_ID = SL.PACKLIST_ID 
		AND RL.PACKLIST_LINE_NO = SL.LINE_NO
		AND RL.PACKLIST_ID IS NOT NULL
	INNER JOIN SHIPPER S
		ON S.PACKLIST_ID = SL.PACKLIST_ID 
	INNER JOIN RECEIVABLE R
		ON RL.INVOICE_ID = R.INVOICE_ID
	INNER JOIN CUSTOMER_ORDER CO
		ON COL.CUST_ORDER_ID = CO.ID
	LEFT OUTER JOIN DEMAND_SUPPLY_LINK DSL
		ON DSL.DEMAND_TYPE = 'CO'
		AND DSL.DEMAND_BASE_ID = COL.CUST_ORDER_ID
		AND DSL.DEMAND_SEQ_NO = COL.LINE_NO
		AND DSL.SUPPLY_BASE_ID IS NULL
	LEFT OUTER JOIN WORK_ORDER WO
		ON (T.TRACE_ID = WO.BASE_ID + '/' + WO.LOT_ID + '/' + WO.SUB_ID)
		AND WO.[TYPE] = 'W'
		AND WO.[STATUS] = 'C'
		AND WO.DESIRED_QTY <> 0
		AND WO.SPLIT_ID = '0'
	WHERE S.SHIPPED_DATE >= @STARTDATE
		AND SL.SHIPPED_QTY <> 0
	) A
