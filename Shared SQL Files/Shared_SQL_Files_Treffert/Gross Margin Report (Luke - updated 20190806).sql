, CTE_TRACE_COSTS as
(SELECT CUST_ORDER_ID
	, LINE_NO
	, SUM(TRACE_BURDEN_COST) AS TOTAL_BURDEN_COST
	, SUM(TRACE_MATERIAL_COST) AS TOTAL_MATERIAL_COST
	, SUM(TRACE_LABOR_COST) AS TOTAL_LABOR_COST
	, SUM(TRACE_SERVICE_COST) AS TOTAL_SERVICE_COST
	, SUM(TOTAL_TRACE_COST) AS TOTAL_COST
FROM
	(SELECT COL.CUST_ORDER_ID
		, COL.LINE_NO
