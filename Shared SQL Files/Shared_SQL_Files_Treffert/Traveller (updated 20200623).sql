SELECT STARTROW_NUM_HRS = A.ROW_NUM_HRS
	, ENDROW_NUM_HRS = LEAD(A.ROW_NUM_HRS) OVER(PARTITION BY WORKORDER_LOT_ID, WORKORDER_SPLIT_ID, WORKORDER_SUB_ID
		ORDER BY A.ROW_NUM_HRS)
	, STARTSEQ
	, ENDSEQ = LEAD(A.ENDSEQ) OVER(PARTITION BY A.WORKORDER_LOT_ID , A.WORKORDER_SPLIT_ID, A.WORKORDER_SUB_ID
		ORDER BY ROW_NUM_HRS)
	, MIN_HOURS
	, WORKORDER_TYPE
	, WORKORDER_BASE_ID
	, WORKORDER_LOT_ID
	, WORKORDER_SPLIT_ID
	, WORKORDER_SUB_ID
INTO #CTE_TL_SEQS
FROM
	(SELECT H.ROW_NUM_HRS
		, H.IS_LIMIT_STARTER
		, H.MIN_HOURS
		, CASE 
			WHEN H.SEQUENCE_NO = O.SEQUENCE_NO THEN 1
			ELSE 0
		END AS IS_LIMIT_ENDER
		, (SELECT H.SEQUENCE_NO WHERE H.IS_LIMIT_STARTER = 1) AS STARTSEQ
		, (SELECT O.SEQUENCE_NO WHERE H.SEQUENCE_NO = O.SEQUENCE_NO) AS ENDSEQ
		, H.WORKORDER_TYPE
		, H.WORKORDER_BASE_ID
		, H.WORKORDER_LOT_ID
		, H.WORKORDER_SPLIT_ID
		, H.WORKORDER_SUB_ID
		, H.SEQUENCE_NO
		, H.RESOURCE_ID
	FROM #CTE_CHEM_PAINT_OP H
	LEFT OUTER JOIN #CTE_FIRST_PAINT_OPS O
		ON H.WORKORDER_TYPE = O.WORKORDER_TYPE
		AND H.WORKORDER_BASE_ID = O.WORKORDER_BASE_ID
		AND H.WORKORDER_LOT_ID = O.WORKORDER_LOT_ID
		AND H.WORKORDER_SPLIT_ID = O.WORKORDER_SPLIT_ID
		AND H.WORKORDER_SUB_ID = O.WORKORDER_SUB_ID
		AND H.SEQUENCE_NO = O.SEQUENCE_NO
		AND O.ROWNUMPNT = 1
	WHERE (SELECT H.SEQUENCE_NO WHERE IS_LIMIT_STARTER = 1) IS NOT NULL
		OR (SELECT O.SEQUENCE_NO WHERE H.SEQUENCE_NO = O.SEQUENCE_NO) IS NOT NULL
	) AS A

SELECT 'CTE_CHEM_PAINT_OP H' AS MSG, * FROM #CTE_CHEM_PAINT_OP H
SELECT 'CTE_FIRST_PAINT_OPS O' AS MSG, * FROM #CTE_FIRST_PAINT_OPS O
SELECT 'CTE_TL_SEQS' AS MSG, * FROM  #CTE_TL_SEQS

SELECT H.ROW_NUM_HRS
	, H.IS_LIMIT_STARTER
	, H.WORKORDER_TYPE
	, H.WORKORDER_BASE_ID
	, H.WORKORDER_LOT_ID 
	, H.WORKORDER_SPLIT_ID
	, H.WORKORDER_SUB_ID
	, H.SEQUENCE_NO 
	, H.RESOURCE_ID
	, CASE
		WHEN H.ROW_NUM_HRS >= T.STARTROW_NUM_HRS
			AND H.ROW_NUM_HRS <= T.ENDROW_NUM_HRS
		THEN T.MIN_HOURS
	END AS MIN_HOURS
	, CASE
		WHEN H.ROW_NUM_HRS >= T.STARTROW_NUM_HRS
			AND H.ROW_NUM_HRS <= T.ENDROW_NUM_HRS
		THEN 1
	END AS ISLIMITBETWEEN
INTO #CTE_ISLIMITBETWEEN
FROM #CTE_CHEM_PAINT_OP H
LEFT OUTER JOIN #CTE_TL_SEQS T
	ON H.WORKORDER_TYPE = T.WORKORDER_TYPE
	AND H.WORKORDER_BASE_ID = T.WORKORDER_BASE_ID
	AND H.WORKORDER_LOT_ID = T.WORKORDER_LOT_ID
	AND H.WORKORDER_SPLIT_ID = T.WORKORDER_SPLIT_ID
	AND H.WORKORDER_SUB_ID = T.WORKORDER_SUB_ID
WHERE CASE
	WHEN H.ROW_NUM_HRS >= T.STARTROW_NUM_HRS
		AND H.ROW_NUM_HRS <= T.ENDROW_NUM_HRS
		THEN 1
	END IS NOT NULL
GROUP BY H.ROW_NUM_HRS
	, H.MIN_HOURS
	, H.IS_LIMIT_STARTER
	, H.WORKORDER_TYPE
	, H.WORKORDER_BASE_ID
	, H.WORKORDER_LOT_ID
	, H.WORKORDER_SPLIT_ID
	, H.WORKORDER_SUB_ID
	, H.SEQUENCE_NO
	, H.RESOURCE_ID
	, CASE
		WHEN H.ROW_NUM_HRS >= T.STARTROW_NUM_HRS
			AND H.ROW_NUM_HRS <= T.ENDROW_NUM_HRS
			THEN T.MIN_HOURS
		END
	, CASE
		WHEN H.ROW_NUM_HRS >= T.STARTROW_NUM_HRS
			AND H.ROW_NUM_HRS <= T.ENDROW_NUM_HRS
			THEN 1
		END

SELECT 'CTE_ISLIMITBETWEEN' AS MSG, * FROM #CTE_ISLIMITBETWEEN

