-- GET HOURS
SELECT ROW_NUMBER() OVER(ORDER BY  O.WORKORDER_BASE_ID, O.WORKORDER_LOT_ID
		, O.WORKORDER_SPLIT_ID, O.WORKORDER_SUB_ID, O.SEQUENCE_NO ) AS ROW_NUM_HRS
	, O.WORKORDER_TYPE
	, O.WORKORDER_BASE_ID
	, O.WORKORDER_LOT_ID
	, O.WORKORDER_SPLIT_ID
	, O.WORKORDER_SUB_ID
	, O.SEQUENCE_NO
	, O.RESOURCE_ID
	, CASE 
		WHEN EXISTS (SELECT 1 FROM @REPORTSETTINGS RS 
			WHERE RS.START_OP_TO_SOLGEL = 1 AND O.RESOURCE_ID = RS.RESOURCE
			) THEN 1 
		ELSE 0 
	END AS IS_LIMIT_STARTER
	, CASE 
		WHEN (SELECT CONVERT(VARCHAR(12), CONVERT(SMALLINT,NUMBER_VAL))
			FROM USER_DEF_FIELDS UDF
			WHERE UDF.PROGRAM_ID = 'VMMFGWIN_OP'
			AND UDF.ID = 'UDF-0000089'             
			AND UDF.DOCUMENT_ID = 'W~' + O.WORKORDER_BASE_ID + '~' + O.WORKORDER_SUB_ID + '~' + O.WORKORDER_LOT_ID + '~' 
				+ O.WORKORDER_SPLIT_ID + '~' + CONVERT(NVARCHAR(3), O.SEQUENCE_NO)) IS NOT NULL 
			THEN (SELECT 'TIME LIMIT ' + CONVERT(VARCHAR(12),CONVERT(SMALLINT,NUMBER_VAL))
				FROM USER_DEF_FIELDS UDF
				WHERE UDF.PROGRAM_ID = 'VMMFGWIN_OP'
				AND UDF.ID = 'UDF-0000089'             
				AND UDF.DOCUMENT_ID = 'W~' + O.WORKORDER_BASE_ID + '~' + O.WORKORDER_SUB_ID + '~' + O.WORKORDER_LOT_ID 
					+ '~' + O.WORKORDER_SPLIT_ID + '~' + CONVERT(NVARCHAR(3), O.SEQUENCE_NO)) + ' HOURS'
		ELSE NULL 
	END AS MIN_HOURS
	, CASE 
		WHEN EXISTS (SELECT 1 FROM @REPORTSETTINGS RS
			WHERE RS.CLEAN_TO_SOLGEL_HRS = 1 AND O.RESOURCE_ID = RS.RESOURCE
			) THEN 1 
		ELSE 0 
	END AS IS_LIMIT_ENDER
INTO #CTE_SOLGEL_OP 
FROM #CTE_OPERATIONS O

