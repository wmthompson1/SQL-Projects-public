DROP TABLE #T1
DROP TABLE #T2

SELECT DOCUMENT_ID
, [UDF-0000074] AS LINE1
, [UDF-0000075] AS LINE2
, [UDF-0000076] AS LINE3
, [UDF-0000077] AS LINE4
, [UDF-0000078] AS LINE5
, [UDF-0000079] AS LINE6
, [UDF-0000080] AS LINE7
, [UDF-0000081] AS LINE8
INTO #T1
FROM (SELECT DOCUMENT_ID
	, ID
	, STRING_VAL
FROM USER_DEF_FIELDS UDF
WHERE UDF.PROGRAM_ID IN (N'VMMFGWIN_OP')
AND LEFT(UDF.DOCUMENT_ID, 1) = 'M' -- IS NOT NULL
AND UDF.ID IN ('UDF-0000074', 'UDF-0000075', 'UDF-0000076', 'UDF-0000077', 'UDF-0000078', 'UDF-0000079', 'UDF-0000080', 'UDF-0000081')
AND UDF.STRING_VAL IS NOT NULL
AND DOCUMENT_ID = 'M~06ND78107-23~0~0~0~200'
--ORDER BY DOCUMENT_ID
) LINES
PIVOT(MAX(STRING_VAL)
	FOR ID IN ([UDF-0000074], [UDF-0000075], [UDF-0000076], [UDF-0000077], [UDF-0000078], [UDF-0000079], [UDF-0000080], [UDF-0000081])
) PVT
--SELECT * FROM #T1

SELECT WORKORDER_BASE_ID
, WORKORDER_LOT_ID
, WORKORDER_SPLIT_ID
, WORKORDER_SUB_ID
, SEQUENCE_NO
, RESOURCE_ID
, OPERATION_TYPE
, CONVERT(NVARCHAR(MAX), O.WORKORDER_TYPE + '~' + O.WORKORDER_BASE_ID + '~' + O.WORKORDER_LOT_ID + '~' + O.WORKORDER_SPLIT_ID + '~' + O.WORKORDER_SUB_ID + '~' + CONVERT(NVARCHAR(MAX), O.SEQUENCE_NO)) AS DOCUMENT_ID
INTO #T2
FROM OPERATION O (NOLOCK)
WHERE WORKORDER_TYPE = 'M'

SELECT O.*
	, UDF.*
FROM #T1 UDF
INNER JOIN #T2 O (NOLOCK)
ON UDF.DOCUMENT_ID = O.DOCUMENT_ID
ORDER BY WORKORDER_BASE_ID
	, WORKORDER_LOT_ID
	, WORKORDER_SPLIT_ID
	, WORKORDER_SUB_ID
	, SEQUENCE_NO
