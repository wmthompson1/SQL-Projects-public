SELECT *
FROM [EC]
LEFT OUTER JOIN EC_LINE ECL
	ON EC.ID = ECL.EC_ID
WHERE EC.[STATUS] != 'C'
	AND EC.ORIGINATOR = 'SHANNEN'
	AND EC.ORIGIN_ID = 'SHANNEN'
	AND EC.ASSIGNED_TO = 'SHANNEN'
	AND EC.CREATED_BY = 'SHANNEN'

SELECT *
FROM [LIVE].[dbo].[EC]
LEFT OUTER JOIN EC_LINE ECL
ON EC.ID = ECL.EC_ID 
WHERE EC.STATUS != 'C'
AND (ORIGINATOR = 'SHANNEN' OR ASSIGNED_TO = 'SHANNEN')

