      ,[GROUP_ID]
      ,[USER_ID]
      ,[IS_LEADER]
      ,[CREATE_DATE]
      ,[USER_SIGNOFF]
  FROM [LIVE].[dbo].[GROUP_USER]
  where user_id = 'shannen'

SELECT ID AS RESOURCE_ID
, [DESCRIPTION]
, USER_6 AS DEPARTMENT
, USER_7 AS PROCESS
, USER_4 AS SUPERVISOR
FROM SHOP_RESOURCE 
WHERE (USER_4 LIKE 'SHANNEN%' OR USER_4 LIKE 'JACOB HELLE%') 
	AND (STATUS IS NULL OR STATUS != 'O')
ORDER BY USER_4

