select *
from part_site p
where ENGINEERING_MSTR is not null 
and not exists
	(select 1 
	from work_order wo
	where wo.part_id = p.part_id
	and wo.BASE_ID = p.PART_ID
	and wo.lot_id = p.ENGINEERING_MSTR
	and wo.[type] = 'M'
	)
