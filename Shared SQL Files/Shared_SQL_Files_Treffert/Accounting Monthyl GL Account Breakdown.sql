, A.[TYPE]
, A.PARENT_ACCT_ID
, AB.ACCT_YEAR
, AB.ACCT_PERIOD
, AB.DEBIT_AMOUNT
, AB.CREDIT_AMOUNT
, AB.CURR_BALANCE
