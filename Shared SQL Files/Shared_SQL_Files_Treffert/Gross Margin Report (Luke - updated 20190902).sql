SELECT CASE
		WHEN CUSTOMER_ID = 'AMTRIB' THEN 'AMTRIB'
		WHEN CUSTOMER_ID = 'SPIAER' THEN 'SPIAER'
		WHEN REC_AMOUNT < 0 AND REC_QTY < 0 THEN 'RETURN'
		WHEN REC_AMOUNT < 0 AND REC_QTY > 0 THEN 'CREDIT'
		WHEN REC_AMOUNT = 0 AND CHARGE_ALLOC > 0 THEN 'CHARGE'
		WHEN BASE_ID LIKE '%R%' THEN 'REWORK'
		WHEN REC_AMOUNT = 0 AND CHARGE_ALLOC = 0 AND [STATUS] = 'X' THEN 'CANCELLED'
		WHEN REC_AMOUNT > 0 THEN 'INVOICE'
		ELSE 'ISSUES'
	END AS REC_ISSUES
	, MRG.*
	, ((REC_AMOUNT + CHARGE_ALLOC) / ISNULL(REC_QTY, TRACE_QTY)) AS INV_UNIT_PRICE

	, (COL_UNIT_PRICE - ((REC_AMOUNT + CHARGE_ALLOC) / ISNULL(REC_QTY, TRACE_QTY))) AS UNITPRICE_DELTA

	, CASE	--REC_AMOUNT + CHARGES
	--	WHEN WO.BASE_ID LIKE '%R%' THEN 0							--> REWORK 
		WHEN SHIPPED_QTY < 0 OR REC_AMOUNT < 0 THEN REC_AMOUNT		--> RETURN OR CREDIT
		WHEN TRACE_QTY IS NULL THEN CHARGE_ALLOC + REC_AMOUNT
		ELSE ((CHARGE_ALLOC + REC_AMOUNT) / ISNULL(REC_QTY, TRACE_QTY)) * (TRACE_QTY)
	END AS TOTAL_PRICE  
	, (TRACE_BURDEN_COST + TRACE_MATERIAL_COST + TRACE_LABOR_COST + TRACE_SERVICE_COST) AS TOTAL_TRACE_COST
	, (TRACE_BURDEN_COST + TRACE_MATERIAL_COST + TRACE_LABOR_COST + TRACE_SERVICE_COST) / ISNULL(TRACE_QTY, REC_QTY) AS TRACE_UNIT_COST
INTO GROSS_MARGIN
