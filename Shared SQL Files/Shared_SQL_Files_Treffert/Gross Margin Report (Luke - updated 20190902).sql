, CTE_CHGAMTS AS
(SELECT CTE1.CUST_ORDER_ID
	, CTE1.TOTAL_AMT_ORDERED
	, CTE2.LINE_COUNT
	, CTE1.TOTAL_AMT_ORDERED / CTE2.LINE_COUNT AS CHARGE_ALLOC
FROM CTE_CHARGES CTE1
INNER JOIN CTE_LINES CTE2
	ON CTE1.CUST_ORDER_ID = CTE2.CUST_ORDER_ID
)
