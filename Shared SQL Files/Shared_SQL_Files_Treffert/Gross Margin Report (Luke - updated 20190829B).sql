, CTE_LINKED as 
(SELECT 'LINKED' AS [ORDER_TYPE]
	, T.TRACE_ID
	, SL.TRANSACTION_ID
	, RL.INVOICE_ID
	, RL.LINE_NO AS INV_LINE
	, T.QTY * -1 AS TRACE_QTY
	, RL.PACKLIST_ID
	, S.SHIPPED_DATE
	, CO.CREATE_DATE AS CO_CREATE_DATE
	, COL.CUST_ORDER_ID
	, COL.LINE_NO
	, CO.CUSTOMER_ID
	, COL.PART_ID
	, COL.LINE_STATUS AS SHIP_SHORT
	, COL.ORDER_QTY AS COL_ORDER_QTY
	, COL.TOTAL_SHIPPED_QTY
	, RL.QTY AS REC_QTY
	, COL.UNIT_PRICE AS COL_UNIT_PRICE
	, (RL.AMOUNT / ISNULL(RL.QTY, SL.SHIPPED_QTY)) AS INV_UNIT_PRICE
	, RL.AMOUNT AS REC_AMOUNT
	, CASE
		WHEN SL.SHIPPED_QTY < 0 OR RL.AMOUNT < 0 THEN 0
		WHEN WO.BASE_ID LIKE '%R%' THEN 0
		ELSE ISNULL(CTE01.CHARGE_ALLOC, 0) 
	END AS CHARGE_ALLOC
	, SL.SHIPPED_QTY
	, CASE	
		WHEN SL.SHIPPED_QTY < 0 OR RL.AMOUNT < 0 THEN RL.AMOUNT
		WHEN WO.BASE_ID LIKE '%R%' THEN 0
		WHEN T.QTY IS NULL THEN ISNULL(CTE01.CHARGE_ALLOC, 0) + (RL.AMOUNT) 
		ELSE ((ISNULL(CTE01.CHARGE_ALLOC, 0) + RL.AMOUNT) / ISNULL(RL.QTY, SL.SHIPPED_QTY)) * (T.QTY * -1)
	END AS TOTAL_PRICE
	, WO.[STATUS]
	, WO.CREATE_DATE AS WO_CREATE_DATE
	, WO.BASE_ID
	, WO.LOT_ID
	, WO.SPLIT_ID
	, WO.SUB_ID
	, WO.DESIRED_QTY
	, CASE
		WHEN RL.AMOUNT < 0 THEN 0
		WHEN T.TRACE_ID IS NULL THEN WO.ACT_BURDEN_COST
		ELSE ((T.QTY * -1) * (WO.ACT_BURDEN_COST / WO.DESIRED_QTY)) 
	END AS TRACE_BURDEN_COST
	, CASE
		WHEN RL.AMOUNT < 0 THEN 0
		WHEN T.TRACE_ID IS NULL THEN WO.ACT_MATERIAL_COST
		ELSE ((T.QTY * -1) * (WO.ACT_MATERIAL_COST / WO.DESIRED_QTY)) 
	END AS TRACE_MATERIAL_COST
	, CASE
		WHEN RL.AMOUNT < 0 THEN 0
		WHEN T.TRACE_ID IS NULL THEN WO.ACT_LABOR_COST
		ELSE ((T.QTY * -1) * (WO.ACT_LABOR_COST / WO.DESIRED_QTY)) 
	END AS TRACE_LABOR_COST
	, CASE
		WHEN RL.AMOUNT < 0 THEN 0
		WHEN T.TRACE_ID IS NULL THEN WO.ACT_SERVICE_COST
		ELSE ((T.QTY * -1) * (WO.ACT_SERVICE_COST / WO.DESIRED_QTY)) 
	END AS TRACE_SERVICE_COST
	, CASE
		WHEN RL.AMOUNT < 0 THEN 0
		WHEN T.TRACE_ID IS NULL THEN (WO.ACT_BURDEN_COST + WO.ACT_MATERIAL_COST + WO.ACT_LABOR_COST + WO.ACT_SERVICE_COST)
		ELSE ((T.QTY * -1) * ((WO.ACT_BURDEN_COST + WO.ACT_MATERIAL_COST + WO.ACT_LABOR_COST + WO.ACT_SERVICE_COST) / WO.DESIRED_QTY))
	END AS TOTAL_TRACE_COST
	, CASE
		WHEN RL.AMOUNT < 0 THEN 0
		ELSE ((WO.ACT_BURDEN_COST + WO.ACT_MATERIAL_COST + WO.ACT_LABOR_COST + WO.ACT_SERVICE_COST) / WO.DESIRED_QTY) 
	END AS TRACE_UNIT_COST
FROM SHIPPER_LINE SL
INNER JOIN SHIPPER S
	ON SL.PACKLIST_ID = S.PACKLIST_ID 
INNER JOIN CUST_ORDER_LINE COL
	ON SL.CUST_ORDER_ID = COL.CUST_ORDER_ID
	AND SL.CUST_ORDER_LINE_NO = COL.LINE_NO
INNER JOIN CUSTOMER_ORDER CO
	ON COL.CUST_ORDER_ID = CO.ID
INNER JOIN DEMAND_SUPPLY_LINK DSL
	ON DSL.DEMAND_TYPE = 'CO'
	AND DSL.DEMAND_BASE_ID = COL.CUST_ORDER_ID
	AND DSL.DEMAND_SEQ_NO = COL.LINE_NO
INNER JOIN WORK_ORDER WO
    ON DSL.SUPPLY_TYPE = 'WO' 
    AND DSL.SUPPLY_BASE_ID = WO.BASE_ID
    AND DSL.SUPPLY_LOT_ID = WO.LOT_ID
    AND DSL.SUPPLY_SPLIT_ID = WO.SPLIT_ID
    AND DSL.SUPPLY_SUB_ID = WO.SUB_ID 
	AND WO.[TYPE] = 'W'
INNER JOIN RECEIVABLE_LINE RL
	ON RL.CUST_ORDER_ID = COL.CUST_ORDER_ID
	AND RL.CUST_ORDER_LINE_NO = COL.LINE_NO
	AND RL.PACKLIST_ID = SL.PACKLIST_ID
	AND RL.PACKLIST_LINE_NO = SL.LINE_NO
INNER JOIN RECEIVABLE R
	ON RL.INVOICE_ID = R.INVOICE_ID
LEFT OUTER JOIN TRACE_INV_TRANS T
	ON SL.TRANSACTION_ID = T.TRANSACTION_ID
LEFT OUTER JOIN CTE_CHGAMTS CTE01
	ON COL.CUST_ORDER_ID = CTE01.CUST_ORDER_ID
WHERE RL.PACKLIST_ID IS NOT NULL
	AND S.SHIPPED_DATE >= @STARTDATE
	AND COL.PART_ID IS NOT NULL
	AND SL.SHIPPED_QTY <> 0
)
