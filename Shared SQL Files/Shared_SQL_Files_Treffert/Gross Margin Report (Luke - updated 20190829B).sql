WITH CTE_CHARGES AS
(SELECT COL.CUST_ORDER_ID
	, SUM(COL.UNIT_PRICE * COL.ORDER_QTY) AS TOTAL_AMT_ORDERED
FROM CUST_ORDER_LINE COL
INNER JOIN CUSTOMER_ORDER CO
	ON COL.CUST_ORDER_ID = CO.ID
WHERE COL.PART_ID IS NULL
	AND COL.SERVICE_CHARGE_ID IS NOT NULL
	AND CO.CREATE_DATE >= '1/1/2015'
	AND CO.[STATUS] != 'X'
	AND COL.ORDER_QTY > 0
GROUP BY COL.CUST_ORDER_ID
)
--SELECT * FROM CTE_CHARGES

--> remove reworks!!
, CTE_LINES AS
(SELECT COL.CUST_ORDER_ID
	, COUNT(COL.LINE_NO) AS LINE_COUNT
FROM CUST_ORDER_LINE COL
INNER JOIN CUSTOMER_ORDER CO
	ON COL.CUST_ORDER_ID = CO.ID
INNER JOIN CTE_CHARGES CTE
	ON COL.CUST_ORDER_ID = CTE.CUST_ORDER_ID
LEFT OUTER JOIN DEMAND_SUPPLY_LINK DSL
	ON DSL.DEMAND_TYPE = 'CO'
	AND DSL.DEMAND_BASE_ID = COL.CUST_ORDER_ID
	AND DSL.DEMAND_SEQ_NO = COL.LINE_NO
WHERE COL.PART_ID IS NOT NULL
	AND (DSL.SUPPLY_BASE_ID NOT LIKE '%R%' 
		OR DSL.SUPPLY_BASE_ID IS NULL)
GROUP BY COL.CUST_ORDER_ID
)
--SELECT * FROM CTE_LINES

, CTE_CHGAMTS AS
(SELECT CTE1.CUST_ORDER_ID
	, CTE1.TOTAL_AMT_ORDERED
	, CTE2.LINE_COUNT
	, CTE1.TOTAL_AMT_ORDERED / CTE2.LINE_COUNT AS CHARGE_ALLOC
FROM CTE_CHARGES CTE1
INNER JOIN CTE_LINES CTE2
	ON CTE1.CUST_ORDER_ID = CTE2.CUST_ORDER_ID
)
