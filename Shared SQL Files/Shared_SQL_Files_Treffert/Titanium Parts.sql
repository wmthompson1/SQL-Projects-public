	  p.[PURCHASED],
	  ps.[PURCHASED],
	  p.[FABRICATED],
	  ps.[FABRICATED],
	  p.[PLANNING_LEADTIME],
	  ps.[PLANNING_LEADTIME]
  FROM LIVE.dbo.PART AS P
	  INNER JOIN LIVE.dbo.PART_SITE AS ps
	  ON P.ID = ps.PART_ID
	  INNER JOIN LIVE.dbo.USER_DEF_FIELDS AS UDF
	  ON P.ID = UDF.DOCUMENT_ID
		AND UDF.ID = 'UDF-0000035'
		AND UDF.STRING_VAL LIKE '%titan%'
