DECLARE @ReportSettings AS TABLE (Resource NVARCHAR(30), [1000] INT, [1200] INT, [600] INT, alo INT, ano INT, Blast INT, Boric INT, Chrome INT, Deburr INT, Pen INT, Polish INT, Prime INT, ShotPeen INT, Sulf INT, TopCoat INT)
INSERT INTO @ReportSettings
EXEC LIVESupplemental..usp_GetReportSettings 'ROYMFG Parts List'

;
WITH CTE_CPE AS 
(SELECT CUSTOMER_ID
	, PART_ID
	, MAX(EFFECTIVE_DATE) AS EFF_DATE
	, MAX(ISNULL(DISCONTINUE_DATE, GETDATE() + 1)) AS DISC_DATE
	, DEFAULT_UNIT_PRICE
FROM CUST_PRICE_EFFECT 
WHERE CUSTOMER_ID = 'ROYMFG'
	AND ISNULL(DISCONTINUE_DATE, GETDATE() + 1) > GETDATE()
	AND ISNULL(EFFECTIVE_DATE, GETDATE() - 1) <= GETDATE()
GROUP BY CUSTOMER_ID, PART_ID
	, DEFAULT_UNIT_PRICE
)

SELECT CO.CUSTOMER_ID
	, WO.PART_ID
	, UDF26.NUMBER_VAL AS SURFACE_AREA
	, UDF35.STRING_VAL AS MATL_TYPE
	, UDF36.STRING_VAL AS ALLOY
	, UDF37.NUMBER_VAL AS [LENGTH]
	, UDF38.NUMBER_VAL AS [WIDTH]
	, UDF39.NUMBER_VAL AS [THICKNESS]
	, WO.BASE_ID
	, WO.LOT_ID
	, WO.SPLIT_ID
	, WO.SUB_ID
	, WO.DESIRED_WANT_DATE
	, WO.DESIRED_QTY
	, CPE.EFF_DATE
	, CPE.DISC_DATE
	, CPE.DEFAULT_UNIT_PRICE AS CONTRACT_PRICE
	, WO.[ACT_LABOR_COST]
	, WO.[ACT_BURDEN_COST]
	, WO.[ACT_MATERIAL_COST] + WO.[ACT_LABOR_COST] + WO.[ACT_BURDEN_COST] + WO.[ACT_SERVICE_COST] AS TotalActualCost
	, (WO.[ACT_MATERIAL_COST] + WO.[ACT_LABOR_COST] + WO.[ACT_BURDEN_COST] + WO.[ACT_SERVICE_COST])/WO.DESIRED_QTY AS AvgActualCost
	, [dbo].[sfn_All_OpTypes](wo.[type], WO.BASE_ID, WO.LOT_ID, WO.SPLIT_ID, WO.SUB_ID) AS OpTypes
	, [dbo].[sfn_all_operations](wo.[type], WO.BASE_ID, WO.LOT_ID, WO.SPLIT_ID, WO.SUB_ID) AS PROCESSES
	, O.RESOURCE_ID
	, CASE WHEN EXISTS (
				SELECT 1 
				FROM @ReportSettings S
				WHERE S.Resource = O.RESOURCE_ID
					AND ISNULL(S.Pen, -1) <> -1
					)
			THEN 'YES'
		ELSE 'NO'
	END AS PENETRANT
	, CASE 
		WHEN EXISTS (
				SELECT 1 
				FROM @ReportSettings S
				WHERE S.Resource = O.RESOURCE_ID
					AND ISNULL(S.ShotPeen, -1) <> -1
				)
			THEN 'YES'
		ELSE 'NO'
	END AS SHOTPEEN
	, CASE WHEN EXISTS (
				SELECT 1 
				FROM @ReportSettings S
				WHERE ISNULL(S.Prime, -1) <> -1
					AND EXISTS (
						SELECT 1 
						FROM OPERATION O
						WHERE S.RESOURCE = O.RESOURCE_ID
							AND WO.BASE_ID = O.WORKORDER_BASE_ID
							AND WO.LOT_ID = O.WORKORDER_LOT_ID
							AND WO.SPLIT_ID = O.WORKORDER_SPLIT_ID
							AND WO.SUB_ID = O.WORKORDER_SUB_ID
							AND WO.[TYPE] = O.WORKORDER_TYPE
						)
					)
			THEN (SELECT OPERATION_TYPE 
			FROM OPERATION O2
			WHERE EXISTS (
				SELECT 1 
				FROM @ReportSettings S
				WHERE ISNULL(S.Prime, -1) <> -1
					AND S.Resource = O2.RESOURCE_ID
				)
			
			
		ELSE ''
	END AS PRIME
	, CASE WHEN EXISTS (
				SELECT 1 
				FROM @ReportSettings S
				WHERE S.Resource = O.RESOURCE_ID
					AND ISNULL(S.TopCoat, -1) <> -1
					)
			THEN O.OPERATION_TYPE
		ELSE ''
	END AS TOPCOAT
