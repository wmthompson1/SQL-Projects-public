FROM WORK_ORDER WO
INNER JOIN [LIVESupplemental].[dbo].[PartID_ReportSettings] P
	ON WO.PART_ID = P.Part_ID

--INNER JOIN OPERATION O
--		ON WO.[TYPE] = O.WORKORDER_TYPE
--		AND WO.BASE_ID = O.WORKORDER_BASE_ID
--		AND WO.LOT_ID = O.WORKORDER_LOT_ID
--		AND WO.SPLIT_ID = O.WORKORDER_SPLIT_ID
--		AND WO.SUB_ID = O.WORKORDER_SUB_ID
--		AND EXISTS (
--			SELECT 1
--			FROM LIVESupplemental..ResourceID_ReportSettings S
--			WHERE S.Report_Name = 'ROYMFG Parts List'
--				AND S.Resource_ID = O.RESOURCE_ID
--		)
LEFT OUTER JOIN CTE_CPE CPE
	ON CPE.PART_ID = WO.PART_ID
LEFT OUTER JOIN DEMAND_SUPPLY_LINK DSL
	ON DSL.SUPPLY_TYPE = 'WO' 
	AND DSL.SUPPLY_BASE_ID = WO.BASE_ID
	AND DSL.SUPPLY_LOT_ID = WO.LOT_ID
	AND DSL.SUPPLY_SPLIT_ID = WO.SPLIT_ID
	AND DSL.SUPPLY_SUB_ID = WO.SUB_ID 
LEFT OUTER JOIN CUSTOMER_ORDER CO
	ON DSL.DEMAND_TYPE = 'CO'
	AND DSL.DEMAND_BASE_ID = CO.ID  
LEFT OUTER JOIN USER_DEF_FIELDS UDF26
-- SURFACE AREA
	ON UDF26.DOCUMENT_ID = WO.PART_ID
	AND UDF26.PROGRAM_ID = 'VMPRTMNT'
	AND UDF26.ID = 'UDF-0000026'
LEFT OUTER JOIN USER_DEF_FIELDS UDF35
-- MATERIAL TYPE
	ON UDF35.DOCUMENT_ID = WO.PART_ID
	AND UDF35.PROGRAM_ID = 'VMPRTMNT'
	AND UDF35.ID = 'UDF-0000035'
LEFT OUTER JOIN USER_DEF_FIELDS UDF36
-- ALLOY
	ON UDF36.DOCUMENT_ID = WO.PART_ID
	AND UDF36.PROGRAM_ID = 'VMPRTMNT'
	AND UDF36.ID = 'UDF-0000036'
LEFT OUTER JOIN USER_DEF_FIELDS UDF37
-- LENGTH
	ON UDF37.DOCUMENT_ID = WO.PART_ID
	AND UDF37.PROGRAM_ID = 'VMPRTMNT'
	AND UDF37.ID = 'UDF-0000037'
LEFT OUTER JOIN USER_DEF_FIELDS UDF38
-- WIDTH
	ON UDF38.DOCUMENT_ID = WO.PART_ID
	AND UDF38.PROGRAM_ID = 'VMPRTMNT'
	AND UDF38.ID = 'UDF-0000038'
LEFT OUTER JOIN USER_DEF_FIELDS UDF39
-- THICKNESS
	ON UDF39.DOCUMENT_ID = WO.PART_ID
	AND UDF39.PROGRAM_ID = 'VMPRTMNT'
	AND UDF39.ID = 'UDF-0000039'
WHERE WO.[TYPE] = 'W'
	AND WO.DESIRED_QTY > 0
	AND WO.DESIRED_WANT_DATE >= '1/1/2016'
	AND CO.CUSTOMER_ID = 'ROYMFG'

ORDER BY WO.PART_ID
	, WO.BASE_ID
	, WO.LOT_ID
	, WO.SPLIT_ID
	, WO.SUB_ID




