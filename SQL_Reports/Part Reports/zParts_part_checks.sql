WITH CTE_PART_CHECK AS
(SELECT PART_ID
    , REPLACE(REPLACE(REPLACE(PLANNER_CHECK,'OKAY','') + ', '
        + REPLACE(BUYER_CHECK, 'OKAY', '') + ', ' + REPLACE(COMMODITY_CODE_CHECK, 'OKAY', '') + ', '
        + REPLACE(SKILLS_DATA_CHECK, 'OKAY', '') + ', ' + REPLACE(DESCRIPTION_CHECK, 'OKAY', ''), ', ', ''), 'FIX', ',FIX')
    AS ALL_CHECKS
    , CASE 
        WHEN PLANNER_CHECK <> 'OKAY' THEN 1
        ELSE 0
    END AS CNT_PLANNER_CHECK
    , CASE 
        WHEN BUYER_CHECK <> 'OKAY' THEN 1
        ELSE 0
    END AS CNT_BUYER_CHECK
    , CASE 
        WHEN COMMODITY_CODE_CHECK <> 'OKAY' THEN 1
        ELSE 0
    END AS CNT_COMMODITY_CODE_CHECK
    , CASE 
        WHEN SKILLS_DATA_CHECK <> 'OKAY' THEN 1
        ELSE 0
    END AS CNT_SKILLS_DATA_CHECK
    , CASE 
        WHEN HAS_PROFILE = 'N' AND LEFT(PLANNER_USER_ID,3) = 'MFG' THEN 1
        WHEN HAS_PROFILE = 'N' AND COMMODITY_CODE = 'ABRASIVES' THEN 1
        ELSE 0
    END AS CNT_PROFILE_CHECK
FROM #TMP_PART 
)
--select * from CTE_PART_CHECK

, CTE_ERROR_COUNTS AS
(SELECT PART_ID
    , ALL_CHECKS
    , CNT_PLANNER_CHECK + CNT_BUYER_CHECK + CNT_COMMODITY_CODE_CHECK 
        + CNT_SKILLS_DATA_CHECK AS ERROR_COUNT
FROM CTE_PART_CHECK
 )

, CTE_CUSTOMER_SHIPPER AS 
(SELECT COL.PART_ID
    , CO.CUSTOMER_ID
    , UDF.STRING_VAL AS BUSINESS_UNIT
    , MAX(S.SHIPPED_DATE) AS MAX_SHIP_DATE
FROM CUSTOMER_ORDER CO
INNER JOIN USER_DEF_FIELDS UDF
    ON UDF.DOCUMENT_ID = CO.CUSTOMER_ID
    AND UDF.ID = 'UDF-0000072'
INNER JOIN CUST_ORDER_LINE COL
    ON COL.CUST_ORDER_ID = CO.ID
LEFT OUTER JOIN SHIPPER_LINE SL
    ON COL.CUST_ORDER_ID = SL.CUST_ORDER_ID
LEFT OUTER JOIN SHIPPER S
    ON COL.CUST_ORDER_ID = S.CUST_ORDER_ID
    AND COL.LINE_NO = SL.CUST_ORDER_LINE_NO
WHERE  EXISTS (SELECT 1
    FROM PART_SITE_VIEW P
    WHERE P.PART_ID = COL.PART_ID
        AND (P.[STATUS] <> 'O' OR P.[STATUS] IS NULL)
        AND (P.PLANNER_USER_ID NOT IN('FIN','FIN-HOURLY','MFG','MFG-HOURLY','MFG-PLAN','MFG-MRP','MFG-HOURLY-PLAN','PURCHASED','TOOLING','BUSSOL')
            OR P.PLANNER_USER_ID IS NULL)
        OR (P.BUYER_USER_ID IS NULL
            OR LEFT(P.BUYER_USER_ID,2) <>
                (SELECT LEFT(ACCOUNT_ID,2)
                FROM LIVESUPPLEMENTAL.DBO.BUYER_ASSN
                )
        )
    OR (P.COMMODITY_CODE NOT IN ('Abrasives', 'Adhesives', 'CFM-Details', 'CFM-Raw Matl', 'CFM-Standards', 'Chemicals', 
        'Details', 'End Item', 'General', 'Lab Tests', 'Non-Fly Away', 'Paint-Primer', 'Paint-TopCoat', 'Raw Material', 'Standards','Tools')
        OR COMMODITY_CODE IS NULL
        )
)
GROUP BY CO.CUSTOMER_ID
    , UDF.STRING_VAL
    , COL.PART_ID
) 
--OPERATIONS FIRST OP FOR BUSINESS UNIT

, CTE_FIRST_OP AS 
(SELECT [TYPE]
    , BASE_ID
    , LOT_ID
    , SPLIT_ID
    , SUB_ID
    , CASE
        WHEN SKILLS_TYPE = 'A' THEN 'FIN'
        WHEN SKILLS_TYPE = 'M' THEN 'MFG'
        WHEN SKILLS_TYPE = 'B' THEN 'FIN'
        ELSE 'OTHER'
    END AS FIRST_OP_BU
    , SKILLS_TYPE
    , FIRSTLOC
FROM   SKILLS_MASTER_OPS
WHERE  FIRSTLOC IS NOT NULL
)  --33466

, CTE_PURCHASE_ORDERS AS 
(SELECT POL.PART_ID
    , MAX(POL.LAST_RECEIVED_DATE) AS LAST_DATE_PUR
FROM PURC_ORDER_LINE POL
INNER JOIN PURCHASE_ORDER PO
    ON POL.PURC_ORDER_ID = PO.ID
WHERE  PO.[STATUS] <> 'X'
    AND EXISTS (SELECT 1
        FROM PART_SITE_VIEW PSV
        WHERE PSV.PART_ID = POL.PART_ID
    )
GROUP BY POL.PART_ID
)

SELECT P.[STATUS]
    , P.CREATE_DATE
    , CASE 
        WHEN P.CREATE_DATE < '12/25/2017' THEN 'CLEAN UP OLD PARTS'
        ELSE 'CLEAN UP NEW PARTS'
    END AS CLEANUP
    , EC.ALL_CHECKS
    , EC.ERROR_COUNT
    , P.PART_ID
    , P.[DESCRIPTION]
    , CASE 
        WHEN LEFT(PLANNER_USER_ID,3) = 'FIN' THEN 'P1-REVIEW'
        ELSE 'P2-REVIEW'
    END AS FILTER_PARAM
    , P.PLANNER_USER_ID
    , P.BUYER_USER_ID
    , P.Salesperson_ID
    , P.COMMODITY_CODE
    , P.PRODUCT_CODE
    , P.ENGINEERING_MSTR
    , P.FABRICATED
    , P.PURCHASED
    , P.STOCKED
    , P.DETAIL_ONLY
    , P.TOOL_OR_FIXTURE
    , P.HASINVENTORY
    , P.QTY_ON_HAND
    , P.QTY_IN_DEMAND
    , P.CBO_MIN_MAX
    , P.AIRPLANE_MODEL
    , P.AWARD_INFO
    , P.PART_FAMILY
    , P.CUSTOMER_ROLT
    , P.MATERIAL_TYPE
    , P.ALLOY
    , P.THICKNESS_VAL
    , P.WIDTH_VAL
    , P.LENGTH_VAL
    , P.SURFACE_AREA
    , P.HAS_PROFILE
    , FO.FIRST_OP_BU
    , FO.SKILLS_TYPE AS FO_SKILLS_TYPE
    , FO.FIRSTLOC AS FO_FIRSTLOC
    , CS.BUSINESS_UNIT AS CS_BUSINESS_UNIT
    , CS.CUSTOMER_ID
    , CS.MAX_SHIP_DATE
    , PU.LAST_DATE_PUR
FROM #TMP_PART P
LEFT OUTER JOIN CTE_FIRST_OP FO
    ON P.PART_ID = FO.BASE_ID
LEFT OUTER JOIN CTE_CUSTOMER_SHIPPER CS
    ON P.PART_ID = CS.PART_ID -- 134827 
LEFT OUTER JOIN CTE_PURCHASE_ORDERS PU
    ON P.PART_ID = PU.PART_ID
LEFT OUTER JOIN CTE_ERROR_COUNTS EC
    ON P.PART_ID = EC.PART_ID
WHERE EC.ERROR_COUNT <> 0
    AND CASE 
        WHEN LEFT(PLANNER_USER_ID,3) = 'FIN' THEN 'P1-REVIEW'
        ELSE 'P2-REVIEW'
    END in (@part_plant_review)
    AND (YEAR(CS.MAX_SHIP_DATE) < YEAR(GETDATE()) - @max_ship_years
        OR CS.MAX_SHIP_DATE IS NULL)
    AND (YEAR(PU.LAST_DATE_PUR) < YEAR(GETDATE()) - @max_purc_years
        OR PU.LAST_DATE_PUR IS NULL)
    AND (P.CREATE_DATE >= @start_date
    AND P.CREATE_DATE <= @end_date)
    AND P.PURCHASED In (@purchased)
    AND P.Salesperson_ID IN (@buyer_name)
    AND P.COMMODITY_CODE IN (@commodity_code)
