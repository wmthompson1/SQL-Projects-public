SELECT P.[STATUS]
    , P.CREATE_DATE
    , CASE 
        WHEN P.PLANNER_USER_ID NOT IN('FIN','FIN-HOURLY','MFG','MFG-HOURLY','MFG-PLAN','MFG-MRP','MFG-HOURLY-PLAN','PURCHASED','TOOLING','BUSSOL')
            OR P.PLANNER_USER_ID IS NULL THEN 'FIX PLANNER'
        ELSE 'OKAY'
    END AS PLANNER_CHECK
            ,CASE WHEN P.BUYER_USER_ID IS NULL
              THEN  'FIX BUYER'
             WHEN LEFT(P.BUYER_USER_ID,2) <> ISNULL(B.ACCOUNT_ID,'')
             THEN 'FIX BUYER'
             ELSE 'OKAY'
         END AS BUYER_CHECK
    , CASE 
        WHEN P.COMMODITY_CODE NOT IN ('Abrasives', 'Adhesives', 'CFM-Details', 'CFM-Raw Matl', 'CFM-Standards', 'Chemicals', 
            'Details', 'End Item', 'General', 'Lab Tests', 'Non-Fly Away', 'Paint-Primer', 'Paint-TopCoat', 'Raw Material',
            'Standards','Tools') OR COMMODITY_CODE IS NULL THEN 'FIX COMMODITY_CODE'
        ELSE 'OKAY'
    END AS COMMODITY_CODE_CHECK
    , CASE 
        WHEN P.[DESCRIPTION] IS NULL THEN 'FIX DESCRIPTION'
        ELSE 'OKAY'
    END AS DESCRIPTION_CHECK
    , P.PART_ID
    , P.[DESCRIPTION]
    , P.PLANNER_USER_ID
    , ISNULL(P.BUYER_USER_ID,'_blank') AS BUYER_USER_ID
    , ISNULL(B.Salesperson_ID,'_blank') AS Salesperson_ID
    , ISNULL(P.COMMODITY_CODE,'_blank') AS COMMODITY_CODE
    , P.PRODUCT_CODE
    , P.ENGINEERING_MSTR
    , P.FABRICATED
    , P.PURCHASED
    , P.STOCKED
    , P.DETAIL_ONLY
    , P.TOOL_OR_FIXTURE
    --INVENTORY INFORMATION
    , CASE 
        WHEN P.QTY_ON_HAND <> 0 THEN 'HAS INVENTORY'
        ELSE 'NO INVENTORY'
    END AS HASINVENTORY
    , P.QTY_ON_HAND
    , P.QTY_IN_DEMAND
    -- MFG NEEDED INFORMATION
    , P.USER_6 AS CBO_MIN_MAX
    , P.USER_7 AS AIRPLANE_MODEL
    , P.USER_8 AS AWARD_INFO
    , P.USER_9 AS PART_FAMILY
    , P.USER_10 AS CUSTOMER_ROLT
    --SKILLS DATA NEEDED
    , CASE 
        WHEN COMMODITY_CODE IN ('CFM-Standards', 'CFM-Raw Matl') THEN 'OKAY'
        ELSE CASE 
            WHEN TOOL_OR_FIXTURE = 'N' AND PURCHASED = 'N' 
-- TT20190722 - ADDED PER ANNW --> TICKET 2370
                AND COMMODITY_CODE IN ('End Item', 'Details', 'Non-Fly Away')
                    THEN CASE 
                WHEN PSA.MATERIAL_TYPE IS NULL OR PSA.ALLOY IS NULL
                    OR PSA.THICKNESS_VAL IS NULL OR PSA.WIDTH_VAL IS NULL
                    OR PSA.LENGTH_VAL IS NULL THEN 'FIX SKILLS DATA'
                ELSE 'OKAY' 
                END
            ELSE 'OKAY'
            END 
    END AS SKILLS_DATA_CHECK
    , PSA.MATERIAL_TYPE
    , PSA.ALLOY
    , PSA.THICKNESS_VAL
    , PSA.WIDTH_VAL
    , PSA.LENGTH_VAL
    , PSA.SURFACE_AREA
    -- CHECK FOR TRACE PROFILE
    , CASE 
        WHEN TP.PART_ID IS NULL THEN 'N'
        ELSE 'Y'
    END AS HAS_PROFILE
INTO #TMP_PART
FROM PART_SITE_VIEW P
LEFT OUTER JOIN SKILLS_PART_SURFACE_AREA PSA
    ON P.PART_ID = PSA.PART_ID
LEFT OUTER JOIN TRACE_PROFILE TP
    ON P.PART_ID = TP.PART_ID
LEFT OUTER JOIN LIVESupplemental.DBO.BUYER_ASSN B
    ON LEFT(P.BUYER_USER_ID,2) = B.Account_ID
WHERE (P.[STATUS] <> 'O' OR P.[STATUS] IS NULL)
GROUP BY P.[STATUS]
    , P.CREATE_DATE
    , P.PART_ID
    , P.[DESCRIPTION]
    , P.PLANNER_USER_ID
    , P.PRODUCT_CODE
    , P.BUYER_USER_ID
                   , B.ACCOUNT_ID
    , B.Salesperson_ID
    , P.COMMODITY_CODE
    , P.ENGINEERING_MSTR
    , P.FABRICATED
    , P.PURCHASED
    , P.STOCKED
    , P.DETAIL_ONLY
    , P.TOOL_OR_FIXTURE
    , CASE
        WHEN P.QTY_ON_HAND <> 0 THEN 'HAS INVENTORY'
        ELSE 'NO INVENTORY'
    END
    , P.QTY_ON_HAND
    , P.QTY_IN_DEMAND
    , P.USER_6
    , P.USER_7
    , P.USER_8
    , P.USER_9
    , P.USER_10
    , PSA.MATERIAL_TYPE
    , PSA.ALLOY
    , PSA.THICKNESS_VAL
    , PSA.WIDTH_VAL
    , PSA.LENGTH_VAL
    , PSA.SURFACE_AREA
    , CASE 
        WHEN TP.PART_ID IS NULL THEN 'N'
        ELSE 'Y'
    END
